package hr.fer.zemris.java.hw07.shell.nameparsing;

/**
 * Enumeration that defines the padding type used while renaming the file.
 * 
 * @author Dinz
 *
 */
public enum SpacingType {
	/**
	 * Left padding with leading zeros.
	 */
	ZEROING,
	/**
	 * Left padding with leading whitespaces.
	 */
	SPACING,
	/**
	 * No padding.
	 */
	NOTHING,
}
